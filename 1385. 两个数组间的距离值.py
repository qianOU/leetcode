class Solution:
    def findTheDistanceValue(self, arr1, arr2, d: int) -> int:
        arr2.sort()
        n = len(arr2)
        ans = 0

        import bisect
        for  j in arr1:
            idx1 = bisect.bisect_left(arr2, j)
            if idx1<n and arr2[idx1] == j: continue

            idx2 = min(idx1 ,n-1)
            idx1 = max(idx1-1 ,0)
            
            if abs(arr2[idx2] - j) > d and abs(j - arr2[idx1]) > d: 
                ans += 1


        return ans

